class PresetAccount {
  const PresetAccount({
    required this.name,
    required this.nameEn,
    required this.nameJa,
    required this.type,
    required this.category,
    required this.icon,
    required this.color,
    this.isDefault = false,
  });

  final String name;
  final String nameEn;
  final String nameJa;
  final AccountType type;
  final AccountCategory category;
  final String icon;
  final String color;
  final bool isDefault;
